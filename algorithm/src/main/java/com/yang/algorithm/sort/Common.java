package com.yang.algorithm.sort;

/**
 * 排序数据
 */
public class Common {

    public static int[] intArr = {10, 5, 60, 59, 91, 22, 678, 4532, 154, 65, 28, 38, 18, 685, 957, 563, 14};

    public static void print(int[] intArr) {
        for (int i = 0; i < intArr.length; i++) {
            int i1 = intArr[i];
            System.out.print(i1 + " ");
        }
        System.out.println();
    }
}
